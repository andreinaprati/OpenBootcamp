package com.jb;

public class BenvenutoJava {

    public static void main(String[] args) {
        System.out.println("Benvenuto Java");

        byte variable01 = 3;
        System.out.println(variable01);

        short variable02 = 9;
        System.out.println(variable02);

        int variable03 = 21;
        System.out.println(variable03);

        long variable04 = 121;
        System.out.println(variable04);

        float variable05 = 3.3f;
        System.out.println(variable05);

        double variable06 = 11.1d;
        System.out.print(variable06);

        boolean variable07 = false;
        System.out.println(variable07);

        boolean variable08 = true;
        System.out.println(variable08);

        char variable09 = 'a';
        System.out.println(variable09);

        String variable10 = "Andreina Prati";
        System.out.println(variable10);

    }
    // tipo de dato 'entero'
    byte variable01 = 3;
    short variable02 = 9;
    int variable03 = 21;
    long variable04 = 121; // por ejemplo un numero de celular

    // tipo de dato 'decimal' (f) (d)
    float variable05 = 3.3f;
    double variable06 = 11.1d; // o 11.11 en lugar de 11.1f

    // tipo de dato 'booleano' V o F
    boolean variable07 = false;
    boolean variable08 = true;

    // tipo de dato 'String'
    char variable09 = 'a';
    String variable10 = "Andreina Prati";

    }
